// to stop gcc crying
void* memcpy(void* dest, const void* src, size_t n) {
    unsigned char* d = dest;
    const unsigned char* s = src;

    // Copy bytes from source to destination
    while (n--) {
        *d++ = *s++;
    }

    return dest;
}

